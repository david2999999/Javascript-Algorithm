// The 'for' loop is exactly the same as in C and Java
for (var i = 0; i < 10; i++) {
    console.log(i);
}

// The 'while' loop is executed while the condition is true
i = 0;
while (i < 10) {
    console.log(i);
    i++;
}

// The 'do-while' loop is similar to the 'while' loop but the condition is
// evaluated after executing the script
i = 0;
do {
    console.log(i);
    i++;
} while (i < 10);
